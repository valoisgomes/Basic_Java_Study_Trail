public class Operators0 {
    public static void main(String[] args) throws Exception {
        // Operadores de atribuição
        int a = 10;
        int b = 5;

        // Operadores aritméticos
        int soma = a + b;
        int subtracao = a - b;
        int multiplicacao = a * b;
        int divisao = a / b;
        int restoDivisao = a % b;

        // Concatenação de strings usando o operador '+'
        String mensagem = "O resultado da soma é: " + soma;

        System.out.println("Operadores aritméticos:");
        System.out.println("Soma: " + soma);
        System.out.println("Subtração: " + subtracao);
        System.out.println("Multiplicação: " + multiplicacao);
        System.out.println("Divisão: " + divisao);
        System.out.println("Resto da divisão: " + restoDivisao);

        System.out.println("\nConcatenação de strings:");
        System.out.println(mensagem);System.out.println("Hello, World!");
    }
}
